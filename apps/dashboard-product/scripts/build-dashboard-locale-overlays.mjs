for (const [locale, overlay] of Object.entries(OVERLAYS)) {
  const unknown = Object.keys(overlay).filter((key) => !(key in en));
  if (unknown.length > 0) {
    console.error(`Unknown keys in ${locale} overlay:`, unknown.join(', '));
    process.exit(1);
  }
  const path = join(overlayDir, `${locale}.json`);
  writeFileSync(path, `${JSON.stringify(overlay, null, 2)}\n`);
  console.log(`Wrote ${path} (${Object.keys(overlay).length} keys)`);
}
